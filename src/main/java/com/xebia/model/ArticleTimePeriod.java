package com.xebia.model;

public class ArticleTimePeriod {

	private String slugId;
	private TimeRead timeToRead;
	
	public String getSlugId() {
		return slugId;
	}
	public void setSlugId(String slugId) {
		this.slugId = slugId;
	}
	public TimeRead getTimeToRead() {
		return timeToRead;
	}
	public void setTimeToRead(TimeRead timeToRead) {
		this.timeToRead = timeToRead;
	}
	
	
}
